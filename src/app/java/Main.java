package app.java;

public class Main {
    public static void main(String[] args) {
        System.out.println(sum(1,2));
        System.out.println(multiply(3,4));
    }
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int multiply(int a, int b) {
        return a * b;
    }
}